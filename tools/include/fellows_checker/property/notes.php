            <h3>使い方</h3>
            <ul>
                <li>フリーカンパニー、リンクシェルを指定してキャラクターの一覧が表示できます。</li>
                <li>キャラクターの一覧には、ジョブ・クラスのレベル、マウント、ミニオンの取得状況を表示できます。</li>
                <li>表示する項目は複数選択できます。</li>
            </ul>
            <ul>
                <li>キャラクターの一覧の表示方法は3種類あります。</li>
                <li>「フリーカンパニーで一覧を表示」：FCを名前、または所属しているキャラクター名から検索して、選択したFCの所属メンバーを一覧に表示。</li>
                <li>「リンクシェルで一覧を表示」：リンクシェルを名前から検索して、選択したリンクシェルの所属メンバーを一覧に表示。</li>
                <li>「キャラクターを一覧に追加」：キャラクターを名前で検索して、1人ずつ一覧に追加。</li>
            </ul>
            <ul>
                <li>フリーカンパニー、リンクシェル、キャラクターを検索する際は、名前（一部でも可）とデータセンター/ワールドが指定できます。</li>
                <li>名前未入力、データセンター/ワールド未指定でも検索できますが、該当件数が多いと先頭50件までしか表示されません。</li>
            </ul>
            <ul>
                <li>キャラクターの一覧表示は、人数が多いと少し時間がかかるかもしれません。</li>
                <li>キャラクターは、名前の順で並び替えて表示しています。</li>
                <li>キャラクターは、－ボタンを押すと一覧上から削除できます。</li>
            </ul>
            <ul>
                <li>キャラクターの一覧に表示する項目は、3つに分類されたリストから複数選択できます。</li>
                <li>「ジョブ / クラス」：ジョブ/クラス名のリストから選択。選択したジョブ/クラスのレベルを表示します。</li>
                <li>「マウント」：マウントのリストから選択。選択したマウントを所持していればアイコンを表示します。</li>
                <li>「ミニオン」：ミニオンのリストから選択。選択したミニオンを所持していればアイコンを表示します。</li>
            </ul>
            <ul>
                <li>マウントとミニオンは、検索ボックスに任意の文字を入力すると、その文字を含む項目の中から選択できます。</li>
                <li>それぞれのリストから選択すると、一覧に表示する項目リストに追加されていきます。</li>
                <li>『追加した項目を一覧に表示』ボタンを押すと、追加した項目リストの内容でキャラクターの一覧上にアイコンを表示します。</li>
                <li>追加した項目は、×ボタンを押すと削除できます。</li>
                <li>項目リストに追加、削除をしてから『追加した項目を一覧に表示』ボタンを押せば、現在追加している項目の表示に更新されます。</li>
            </ul>
            <ul>
                <li>キャラクターの一覧上に表示する項目は、すべてアイコンで表示されます。</li>
                <li>マウントとミニオンは、所持していればそのマウント、ミニオンの画像を表示します。</li>
                <li>ジョブ/クラスは、そのジョブ/クラスのアイコンと、その右隣にレベルの数字のアイコンを、2個セットで表示します。</li>
                <li>各アイコンは、マウスオーバー、またはタップすると、何の項目なのかをフキダシで表示します。</li>
                <li>クラフターのマイスターを取得している場合は、レベルの文字色とフキダシの内容がマイスターの表示に変わります。</li>
            </ul>
