            <h3>使い方</h3>
            <ul>
                <li>フリーカンパニー、リンクシェルを指定してキャラクターの一覧が表示できます。</li>
                <li>キャラクターの一覧には、ジョブ・クラスのレベル、マウント、ミニオンの取得状況が表示できます。</li>
                <li>それぞれの表示する項目は、セレクトボックスから複数選択できます。</li>
            </ul>
            <ul>
                <li>キャラクター一覧の表示方法は3種類あります。</li>
                <li>フリーカンパニーで一覧を表示する場合は、フリーカンパニー名、または所属しているキャラクター名から検索できます。</li>
                <li>リンクシェルで一覧を表示する場合は、リンクシェル名から検索できます。</li>
                <li>キャラクター単位で検索して、1人ずつ一覧に追加することもできます。</li>
            </ul>
            <ul>
                <li>フリーカンパニー、リンクシェル、キャラクターを検索する際は、名前（一部でも可）とデータセンター/ワールドが指定できます。</li>
                <li>名前未入力、データセンター/ワールド未指定でも検索できますが、該当件数が多いと先頭50件までしか表示されません。</li>
            </ul>
            <ul>
                <li>キャラクターの一覧に表示する項目は、セレクトボックスから選択するとリストに追加されていきます。</li>
                <li>「追加した項目を一覧に表示」ボタンを押すと、リスト内の項目が一覧上にアイコンで表示されます。</li>
                <li>マウントとミニオンは検索ボックスに任意の文字を入力すると、その文字を含む項目だけを選択できます。</li>
            </ul>
        </div>
